"""
Operaciones aritmeticas basicas como suma, resta, multiplicación 
y división, entre 2 numeros y que representan una calculadora sencilla
"""

def suma(num1, num2):
    return num1+num2

def resta(num1, num2):
    return num1-num2

def multiplicacion(num1, num2):
    return num1*num2

def division(num1, num2):
    return num1/num2