def multiplicar(num1, num2):
    """ 
    Función que multiplica dos numeros
    Args:
        num1 (int): _description_
        num2 (int): _description_
    Return:
        Retorna la multiplicación de los argumentos ingresados
    Examples:
        >>> multiplicar(2, 3)
        6
        >>> multiplicar(2,4)
        8
        >>> multiplicar(2,4)
        20
    """
    return num1*num2

print(f"El resultado de la multiplicación es {multiplicar(6,12)} ")